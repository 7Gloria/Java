public class Println {
public static void main(String[] args) {
	System.out.println("Println statement prints a line and next statement goes to next line...");
	
	/*There is also a print() method, which is similar to println().
	The only difference is that print() does not insert a new line at the end of the output */
	
	System.out.print("Print statement prints a line and next statement will be in the same line..");
	System.out.println("Println() Also used for digits and math calculations");
	System.out.println(3);
	System.out.println(2+2);
	System.out.println(2*3);
	System.out.println(7/7);
}
}
